package t4_Conditionalstatement;

public class c5_switchvowels {
//task2 vowels and cosonants by switch statement
	public static void main(String[] args) {
		char a='e';
		
		switch(a) {
		
		case 'a':System.out.println(a+"a is vowels");
		break;
		
		case 'e':System.out.println("e is vowels");
		break;
		
		case 'i':System.out.println("i is vowels");
		break;
		
		case 'o':System.out.println("o is vowels");
		break;
		
		case 'u':System.out.println("u is vowels");
		break;
		
		default:System.out.println("Is consonent");
		break;
		
		
		
		
		}

	}

}
